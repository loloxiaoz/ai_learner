export interface AttentionExample {
  id: string;
  sentence: string;
  tokens: string[];
  heads: {
    name: string;
    description: string;
    weights: number[][]; // [from][to]，每行已归一化（sum=1）
  }[];
}

// softmax 辅助
function softmax(arr: number[]): number[] {
  const max = Math.max(...arr);
  const exps = arr.map((x) => Math.exp(x - max));
  const sum = exps.reduce((a, b) => a + b, 0);
  return exps.map((x) => x / sum);
}

function makeWeights(raw: number[][]): number[][] {
  return raw.map((row) => softmax(row));
}

